class Gallery extends Component {
  state = {image: imagesList[0].imageUrl, imageAlt: imagesList[0].imageAltText}

  updateImage = id => {
    const {image} = this.state
    const displayImage = imagesList.filter(each => each.id === id)
    const {imageUrl, imageAltText} = displayImage[0]
    this.setState({image: imageUrl, imageAlt: imageAltText})
    return image
  }

  render() {
    const {image, imageAlt} = this.state
    return (
      <div className="gallery-page-container">
        <img src={image} alt={imageAlt} className="gallery" />
        <h1 className="header">Nature Photography</h1>
        <p className="para-text">Nature Photography by Rahul</p>
        <ul className="listItems-container">
          {imagesList.map(eachImageList => (
            <ThumbnailItem
              eachImageList={eachImageList}
              key={eachImageList.id}
              updateImage={this.updateImage}
              image={image}
            />
          ))}
        </ul>
      </div>
    )
  }
}

export default Gallery
